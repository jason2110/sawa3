# Relational Operators in Python

age=20
if age<30:
	print("You are young!") 
else:
	print("You are old!")

age=20
if age==30:
	print("Both ages are same!") 
else:
	print("Both ages are not the same!")

age=20
if age!=30:
	print("Both ages are same!") 
else:
	print("Both ages are not the same!")