# If statements in PY3

studentMarks=101

if studentMarks>50 and studentMarks<=59:
    print("You are pass and your grade is D")
elif studentMarks>=60 and studentMarks<=69:
    print("You are pass and your grade is C")
elif studentMarks>=70 and studentMarks<=79:
    print("You are pass and your grade is B")
elif studentMarks>=80:
    print("You are pass and your grade is A")
else:
    print("You are fail")